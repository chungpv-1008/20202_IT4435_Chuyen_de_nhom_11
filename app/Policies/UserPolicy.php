     * @param  \App\User  $user
     * @param  \App\User  $model
     * @return mixed
     */
    public function view(User $user, User $userNeedAuthor)
    {
        if ($user->id === $userNeedAuthor->id) {
            return true;
        }
        if ($user->role_id === config('user.employer')) {
            $jobs = $userNeedAuthor->jobs()->where('company_id', $user->company->id)->get();

            return $jobs->count();
        }

        return false;
    }

